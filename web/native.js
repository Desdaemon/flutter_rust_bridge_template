
let wasm;

/**
* @returns {number}
*/
export function platform() {
    var ret = wasm.platform();
    return ret >>> 0;
}

/**
* @returns {boolean}
*/
export function rust_release_mode() {
    var ret = wasm.rust_release_mode();
    return ret !== 0;
}

